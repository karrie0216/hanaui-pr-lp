let selectedQ1 = null;

function selectQ1(value) {
  selectedQ1 = value;
  document.querySelectorAll('[data-q="1"]').forEach(el => {
    el.classList.toggle('selected', el.dataset.value === value);
  });
  updateProgress(2);
  setTimeout(() => showStep(2), 200);
}

function selectQ2(value) {
  if (!selectedQ1) return;
  document.querySelectorAll('[data-q="2"]').forEach(el => {
    el.classList.toggle('selected', el.dataset.value === value);
  });
  const key = `${selectedQ1}_${value}`;
  const result = resultMap[key];
  if (!result) return;
  updateProgress(3);
  setTimeout(() => showResult(result.persona, result.highlights), 200);
}

function showStep(n) {
  document.querySelectorAll('.quiz-step').forEach((el, i) => {
    el.classList.toggle('active', i + 1 === n);
  });
  document.getElementById('quiz-result').classList.remove('active');
  scrollToQuiz();
}

function showResult(personaKey, highlights) {
  const data = personaData[personaKey];
  const resultEl = document.getElementById('quiz-result');

  document.getElementById('result-img').src = data.img;
  document.getElementById('result-img').alt = data.label;
  document.getElementById('result-tag').textContent = data.label;
  document.getElementById('result-title').textContent = data.tagline;
  document.getElementById('result-desc').textContent = data.desc;

  document.getElementById('result-services').innerHTML = highlights.map(idx => {
    const s = data.services[idx];
    return `<div class="quiz-result__service">
      <p class="quiz-result__service-title">${s.title}</p>
      <p class="quiz-result__service-desc">${s.desc}</p>
    </div>`;
  }).join('');

  document.querySelectorAll('.quiz-step').forEach(el => el.classList.remove('active'));
  resultEl.classList.add('active');
  scrollToQuiz();
}

function updateProgress(step) {
  document.querySelectorAll('.quiz-progress__step').forEach((el, i) => {
    el.classList.remove('active', 'done');
    if (i + 1 < step)      el.classList.add('done');
    else if (i + 1 === step) el.classList.add('active');
  });
}

function resetQuiz() {
  selectedQ1 = null;
  document.querySelectorAll('.quiz-option').forEach(el => el.classList.remove('selected'));
  document.getElementById('quiz-result').classList.remove('active');
  updateProgress(1);
  showStep(1);
}

function scrollToQuiz() {
  document.getElementById('quiz').scrollIntoView({ behavior: 'smooth', block: 'start' });
}
